    private void populateTable() {
         DefaultTableModel dtm = (DefaultTableModel) customerTable.getModel();
        dtm.setRowCount(0);

        for (Customer c : masterClass.getCustomerCatalogue().getCustCat()) {
            Object row[] = new Object[4];
            row[0] = c;
            row[1] = c.getZipCode();
            row[2] = c.getPrevOrders().totalProducts();
            row[3] = c.getPrevOrders().totalPrice();
            dtm.addRow(row);
        }
    }
    
    

