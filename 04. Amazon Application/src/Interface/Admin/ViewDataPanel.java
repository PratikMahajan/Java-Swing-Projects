    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        cards.remove(this);
        CardLayout cLayout = (CardLayout) cards.getLayout();
        cLayout.previous(cards);
    }//GEN-LAST:event_backBtnActionPerformed

    private void selectBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectBtn2ActionPerformed
        if (customerTable.getSelectedRow() >= 0) {
            Customer c = (Customer) customerTable.getValueAt(customerTable.getSelectedRow(), 0);
            ViewCustomerInformation vci = new ViewCustomerInformation(cards, c);
            cards.add("ViewCustomerInformation", vci);
            CardLayout cLayout = (CardLayout) cards.getLayout();
            cLayout.next(cards);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_selectBtn2ActionPerformed

