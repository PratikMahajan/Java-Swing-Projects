    private void addRetailerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRetailerBtnActionPerformed
        AddRetailerPanel addRetailer = new AddRetailerPanel(cards, masterClass.getRetailerCatalogue(), masterClass.getUserAccountDirectory());
        cards.add("AddRetailerJPanel", addRetailer);
        CardLayout cLayout = (CardLayout) cards.getLayout();
        cLayout.next(cards);
    }//GEN-LAST:event_addRetailerBtnActionPerformed

    private void deleteAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAirlinerBtnActionPerformed
        if (retailerTable.getSelectedRow() >= 0) {
            Retailer r = (Retailer) retailerTable.getValueAt(retailerTable.getSelectedRow(), 1);
            masterClass.getRetailerCatalogue().removeRetailer(r);
            populateTable(masterClass.getRetailerCatalogue().getRetailerCatalogue());

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_deleteAirlinerBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        cards.remove(this);
        CardLayout cLayout = (CardLayout) cards.getLayout();
        cLayout.previous(cards);
    }//GEN-LAST:event_backBtnActionPerformed

    private void selectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectBtnActionPerformed
        if (retailerTable.getSelectedRow() >= 0) {
            Retailer r = (Retailer) retailerTable.getValueAt(retailerTable.getSelectedRow(), 1);
            ViewRetailerPanel viewJPanel = new ViewRetailerPanel(cards, r);
            cards.add("ViewRetailerPanel", viewJPanel);
            CardLayout cLayout = (CardLayout) cards.getLayout();
            cLayout.next(cards);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_selectBtnActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Validate.integerCheck(zipTF.getText(), "Zip")) {
            ArrayList<Retailer> searchResult = masterClass.getRetailerCatalogue().searchByZip(zipTF.getText());
            populateTable(searchResult);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

