    private void populateTable(ArrayList<Retailer> list) {
        DefaultTableModel dtm = (DefaultTableModel) retailerTable.getModel();
        dtm.setRowCount(0);

        for (Retailer r : list) {
            Object row[] = new Object[5];
            row[0] = r.getRetailerNumber();
            row[1] = r;
            row[2] = r.getZipCode();
            row[3] = r.getProductCatalogue().getTotalSoldCount();
            row[4] = r.getEarnings();
            dtm.addRow(row);
        }
    }

