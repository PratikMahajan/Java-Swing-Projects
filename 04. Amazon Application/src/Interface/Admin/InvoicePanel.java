    private void payBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payBtnActionPerformed
        if (invoiceListTbl.getSelectedRow() >= 0) {
            RetailerInvoice ri = (RetailerInvoice) invoiceListTbl.getValueAt(invoiceListTbl.getSelectedRow(), 2);
            Retailer r = (Retailer) invoiceListTbl.getValueAt(invoiceListTbl.getSelectedRow(), 1);
            if(r.payRetailer(ri.getInvoiceNumber(), Admin.payRetailer(ri))) {
                JOptionPane.showMessageDialog(null, "Amount paid to "+r.getRetailerName());
                populateInvoiceTbl();
            } else {
                JOptionPane.showMessageDialog(null, "Payment Failed");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_payBtnActionPerformed

