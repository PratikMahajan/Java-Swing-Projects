    private void populateInvoiceTbl() {
        DefaultTableModel dtm = (DefaultTableModel) invoiceListTbl.getModel();
        dtm.setRowCount(0);
        
        for (Retailer r : retailerCatalogue.getRetailerCatalogue()) {
            for (RetailerInvoice ri : r.getRetailerInvoiceList().getInvoiceList()) {
                if (!ri.isPaid()) {
                    Object row[] = new Object[5];
                    row[0] = r.getRetailerNumber();
                    row[1] = r;
                    row[2] = ri;
                    row[3] = ri.getCartProductsSold().getCartCat().size();
                    row[4] = ri.getTotalPriceToBePaid();
                    dtm.addRow(row);
                }
            }
        }
        updateTresury();
    }
    
