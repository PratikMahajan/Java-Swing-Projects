    private void displayTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblResult.getModel();
        dtm.setRowCount(0);
        for(Flight ff:fc.getFleetCat())
        {
            Object[] ob= new Object[3];
            ob[0]=ff;
            ob[1]=ff.getFromLoc();
            ob[2]=ff.getToLoc();
            dtm.addRow(ob);
            
        }
    }
    
    
