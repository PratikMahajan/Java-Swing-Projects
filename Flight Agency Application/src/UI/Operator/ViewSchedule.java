    private void btnViewFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewFlightActionPerformed
        
        int getSelected= tblResult.getSelectedRow();
        if(getSelected>=0)
        {
            Flight arer=new Flight();
            arer=(Flight)tblResult.getValueAt(getSelected, 0);
            if(option==22)
            {
                ViewFlight vfl = new ViewFlight(userProcessContainer, arer, airpC);
                userProcessContainer.add("AddAirplane",vfl);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            if(option==11)
            {
                ViewFlight vfl = new ViewFlight(userProcessContainer, arer);
                userProcessContainer.add("AddAirplane",vfl);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Select Row", "Error", 0);
        
        
        
        
    }//GEN-LAST:event_btnViewFlightActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int getSelected= tblResult.getSelectedRow();
        if(getSelected>=0)
        {
            Flight arer=new Flight();
            arer=(Flight)tblResult.getValueAt(getSelected, 0);
            fc.removeAeroplane(arer);
            displayTable();
        }
        else
            JOptionPane.showMessageDialog(null, "Select Row", "Error", 0);
        
    }//GEN-LAST:event_jButton1ActionPerformed


