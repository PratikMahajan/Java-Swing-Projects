    private void btnViewFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewFlightActionPerformed

        int getSelected= tblResult.getSelectedRow();
        if(getSelected>=0)
        {
            Flight arer=new Flight();
            arer=(Flight)tblResult.getValueAt(getSelected, 1);
            ViewBookedDetails vfl = new ViewBookedDetails(userProcessContainer, arer,cust);
            userProcessContainer.add("viewFlightdetBooked",vfl);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
            JOptionPane.showMessageDialog(null, "Select Row", "Error", 0);
    }//GEN-LAST:event_btnViewFlightActionPerformed

