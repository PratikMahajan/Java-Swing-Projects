    public void getallBookings()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblResult.getModel();
        dtm.setRowCount(0);
        
        ArrayList<Object[]> res;
        res= opc.getAllBookings(cust);
        if(res!=null)
        {
            for(Object[] ob:res)
            {
                Object[] tmp=new Object[4];
                tmp[0]=ob[0];
                tmp[1]=ob[1];
                Flight ff=(Flight)ob[1];
                tmp[2]=ff.getFromLoc();
                tmp[3]=ff.getToLoc();
                
                dtm.addRow(tmp);
                
            }
        }
        
    }
    
    
    
